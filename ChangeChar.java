//how to use changechar
public class ChangeChar {
	public static void main(String[] args) {
	
		//  \t  ：一个制表位长度的空格 ，实现对齐的功能
		System.out.println("北京\t天津\t上海");
		//   \n  ：换行符
		System.out.println("jack\nsmith\nmary");
		//   \\  ：一个\  \\
		System.out.println("C:\\Windows\\System32\\java.exe");
		//   \"  :一个"
		System.out.println("I say :\"i will be free,come on\"");
		//   \'  ：一个'
		System.out.println("I say:\'i will be free,come on\'");
		
		System.out.println("Forrest\rchengdu"); 
	}
}